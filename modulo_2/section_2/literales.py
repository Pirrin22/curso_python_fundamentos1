'''
Los literales son tipos de datos cuyos valores estan determinados por el literal mismo
'''
# Este literal es una cadena de texto (string) (Las cadenas de texto deben tienen que ir envueltas en comillas dobles o comillas simples)
print("Hola, soy Cristian")

# Este literal es un Entero (int) 
print(5)

# Este literal es un Numero de punto flotante (float)
print(5.5)

# Este literal es un Boleano (bool)
print(True)